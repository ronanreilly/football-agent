package manage.my.footballers;

public class Player {
	public int id;
	public String team;
	public String firstName;
	public String lastName;
	public String age;
	public String country_origin;
	public String position;
	public String pref_foot;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getCountry_origin() {
		return country_origin;
	}
	public void setCountry_origin(String country_origin) {
		this.country_origin = country_origin;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getPref_foot() {
		return pref_foot;
	}
	public void setPref_foot(String pref_foot) {
		this.pref_foot = pref_foot;
	}
	
	
	
	
	
	
}
